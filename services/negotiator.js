/**
 * 根据风险ID生成谈判话术
 * @param {string} trapId - 风险ID
 * @param {string} trapName - 风险名称
 * @param {string} matchedClause - 匹配的原文
 * @returns {object} 谈判话术
 */
function generateNegotiationScript(trapId, trapName, matchedClause) {
  const template = NEGOTIATION_TEMPLATES[trapId];

  if (!template) {
    // 通用话术（未知风险类型）
    return {
      opening: `关于「${trapName}」，我有几点想协商`,
      keyPoints: [
        '该条款可能存在不公平之处',
        '建议双方协商修改为更公平的表述',
        '可以引用相关法律规定作为依据'
      ],
      script: `「关于「${trapName}」，我对这个条款有些顾虑。合同中的表述是："${matchedClause || '...'}"，这个约定对乙方来说不太公平。我建议双方协商修改，使其更加公平合理，符合法律规定。您看这个条款能不能调整一下？」`,
      fallback: '如果协商不成，建议咨询专业律师，或考虑是否接受该条款'
    };
  }

  // 个性化替换
  const script = template.script.replace(
    /合同中的表述是："\.\.\."/g,
    matchedClause ? `合同中的表述是："${matchedClause}"` : ''
  );

  return {
    opening: template.opening,
    keyPoints: template.keyPoints,
    script: script,
    fallback: template.fallback
  };
}

/**
 * 批量生成谈判话术
 * @param {Array} traps - 风险列表
 * @returns {Array} 带话术的风险列表
 */
function generateScriptsForTraps(traps) {
  return traps.map(trap => {
    const matchedClause = trap.matchedClauses && trap.matchedClauses.length > 0
      ? trap.matchedClauses[0]
      : '';

    const script = generateNegotiationScript(trap.id, trap.name, matchedClause);

    return {
      ...trap,
      negotiation: script
    };
  });
}

module.exports = {
  generateNegotiationScript,
  generateScriptsForTraps,
  NEGOTIATION_TEMPLATES
};
